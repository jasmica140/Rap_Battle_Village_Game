void cli(){

    for(int k=0; k<mapy; k++){
        for(int i=0; i<mapx;i++){
            cout << MAGENTA << "+-----";
        }
        cout << "+" << endl;

        for(int i=0; i<2; i++){
            for(int j=0; j<(mapx/2)+1; j++){
                cout << CYAN << "|" << MAGENTA << map[j][k].status;
            }
        }
        cout << endl;
    }

    for(int i=0; i<mapx;i++){
        cout << MAGENTA << "+-----";
    }
    cout << "+" << endl;
}

