void mapcli(int playno){

    mvwprintw(win,10,122,"--MAP--");
    int i,j,k;
    for(k=0; k<mapy; k++){
        for(i=0; i<mapx; i++){
            mvwprintw(win,11+(k*2),86+(i*6),"+-----");
        }
        mvwprintw(win,11+(k*2),86+(i*6),"+");

        for(i=0; i<mapy-2; i++){
            for(j=0; j<mapx; j++){
                mvwprintw(win,12+(k*2),86+(j*6), "|%s", map[j][k].status.c_str());
            }
        }
        mvwprintw(win,12+(k*2),86+(j*6), "|");

    }

    for(i=0; i<mapx; i++){
        mvwprintw(win,11+(k*2),86+(i*6),"+-----");
    }
    mvwprintw(win,11+(k*2),86+(i*6),"+");
}

